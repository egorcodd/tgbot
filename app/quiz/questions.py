def question_by_index(idx: int) -> dict | None:
    if 0 <= idx < len(QUESTIONS):
        return QUESTIONS[idx]
    return None


def total_questions() -> int:
    return len(QUESTIONS)


def reaction_for(question: dict, option_key: str) -> str | None:
    reactions = question.get("reactions") or {}
    if option_key in reactions:
        return reactions[option_key]
    for key, value in reactions.items():
        if isinstance(key, tuple) and option_key in key:
            return value
    if "_any" in reactions:
        return reactions["_any"]
    return None
